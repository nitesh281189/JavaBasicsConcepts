package JavaCoding;

public class IncrementandDecrementOperator 
{
	public static void main(String[] args) {
		//Post Increment
		int i=1;
		int j=i++;  //post increment increase the value of i later
		//Give the value to J and then increase by 1 
		System.out.println(i);
		System.out.println(j);
		//Pre Increment
		int i1=1;
		int j1=++i1;
		System.out.println(i1);
		System.out.println(j1);
		//The value of i1 is already incremented thats why 2,2	
		
		//Similarly we will have for decrement also 		
	}
}
